    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // evento agregar
        if(validar()){
            Vuelo vuelo=new Vuelo(
                    txtVuelo.getText(),
                    Integer.parseInt(txtCantidad.getText())
            );
            JOptionPane.showMessageDialog(this, "Se dio de alta un vuelo.");
            txtVuelo.setText("");
            txtCantidad.setText("");
            txtVuelo.requestFocus();
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private boolean validar(){
        //Validar que txtVuelo tenga 7 caracteres
        if(!new Validator(txtVuelo).length(7)) return false;
        //Validar que txtCantidad se un n√∫mero entero entre 40 y 400.
        if(!new Validator(txtCantidad).isInteger(40,400)) return false;
        return true;
    }
    
